/**
 * POST /api/transcribe
 * Accepts an audio file and the associated encounter context, returns a SOAP note.
 *
 * @param {File}   audioFile  — .mp3 or .wav, max 25 MB
 * @param {object} encounter  — selected encounter object (must have .csn and .mrn)
 * @returns {Promise<object>} — { transcript, soap, follow_up_scheduled, ... }
 */
export async function transcribeAudio(audioFile, encounter) {
  const form = new FormData();
  form.append('audio', audioFile);
  if (encounter?.csn) form.append('csn', encounter.csn);
  if (encounter?.mrn) form.append('mrn', encounter.mrn);

  const res = await apiClient.post('/transcribe', form, {
    headers: { 'Content-Type': 'multipart/form-data' },
    timeout: 120000  // 2 min — Whisper (~25s) + Claude (~35s) + Mirth + buffer; overrides the 35s global default
  });
  return res.data;
}

