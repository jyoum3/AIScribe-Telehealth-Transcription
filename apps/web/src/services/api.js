/**
 * AIScribe Web — API Service Client
 *
 * Central Axios instance for all browser-to-Express communication.
 * All requests go to /api/* which Vite proxies to Express on :3001
 * during local development (see vite.config.js).
 *
 * Why a shared instance?
 *   - Authorization header is injected once here, not in every component.
 *   - Base URL is configured in one place — easy to swap for production.
 *   - Response interceptors handle 401 redirects globally.
 */

import axios from 'axios';

const apiClient = axios.create({
  baseURL: '/api',
  headers: { 'Content-Type': 'application/json' },
  timeout: 35000 // 35s — covers Whisper transcription (30s) + buffer
});

// Inject Bearer token from localStorage on every request
apiClient.interceptors.request.use(
  (config) => {
    const token = localStorage.getItem('aiscribe_token');
    if (token) config.headers.Authorization = `Bearer ${token}`;
    return config;
  },
  (error) => Promise.reject(error)
);

// On 401, clear the stored token (session expired / invalid)
apiClient.interceptors.response.use(
  (response) => response,
  (error) => {
    if (error.response?.status === 401) {
      localStorage.removeItem('aiscribe_token');
    }
    return Promise.reject(error);
  }
);

/** POST /api/auth/login */
export async function login(email, password) {
  const res = await apiClient.post('/auth/login', { email, password });
  return res.data;
}

